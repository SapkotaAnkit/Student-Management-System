public class Student
{
    public string ID;
    public string Name;
    public int Age;
    public string Course;
    public int Marks;

    public Student(string ID, string Name, int Age, string Course, int Marks)
    {
        this.ID = ID;
        this.Name = Name;
        this.Age = Age;
        this.Course = Course;
        this.Marks = Marks;
    }
}